# Arithmetic Operations
# Symbol	Name
# +	Addition
# -	Subtraction
# *	Multiplication
# /	Division
# **	Exponentiation
# %	Modulo
# //	Integer or floor  Division


a = 100
b = 3

# addition
print('Sum: ', a + b)

# subtraction
print('Subtraction: ', a - b)

# multiplication
print('Multiplication: ', a * b)

# division - it is treated as a float
print('Division: ', a / b)
print('type after Division : ', type(a / b))

# floor division
print('Floor Division: ', a // b)
print('type after floor Division : ', type(a // b))

# modulo - remainder
print('Modulo: ', a % b)

# exponential a to the power b
print('Power: ', a ** b)
